#include<iostream>
using namespace std;


//普通函数与函数模板的区别
//1.普通函数调用可以发生隐式类型转换
//2.函数模板 用自动类型推导 不可以发生隐式类型转换
//3.函数模板 用显示指定类型，可以发生隐式类型转换

//普通函数
int myAdd01(int a,int b) {
	return a + b;
}

template<class T>
int myAdd02(T a, T b) {
	return a + b;
}

void test01() {
	int a = 10;
	int b = 20;

	cout << myAdd01(a, b) << endl;

	char c = 'c';
	cout << myAdd01(10, c) << endl;
	//上面这个就是普通函数调用时候发生隐式类型转换
}

void test02() {
	int a = 10;
	int b = 20;

	cout << myAdd02(a, b) << endl;

	char c = 'c';
	//cout << myAdd02(10, c) << endl;
	//函数模板在使用自动类型推导时候，不会发生隐式类型转换
}

void test03() {
	int a = 10;
	int b = 20;

	cout << myAdd02(a, b) << endl;

	char c = 'c';
	cout << myAdd02<int>(10, c) << endl;
	//函数模板在使用自动类型推导时候，显式指定类型时，可以发生隐式类型转换
}



